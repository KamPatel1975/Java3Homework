package Java3Homework;

public class WhileLoopOddEven {
    public static void main (String[] args){
        int i= 1;
        while (i<10){
            System.out.println(i*2);
            i++;
        }
        int y = 1;
        while (y<10){
            System.out.println(y*2-1);
            y++;
        }
    }
}
